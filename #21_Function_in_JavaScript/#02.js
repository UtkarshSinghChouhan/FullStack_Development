// JavaScript function called generateGreeting
function generateGreeting(name) {
    return `Hello, ${name}! Welcome to our website.`;
}

// Greet three different people
const person1 = "Alice";
const person2 = "Bob";
const person3 = "Charlie";

console.log(generateGreeting(person1));
console.log(generateGreeting(person2));
console.log(generateGreeting(person3));

// OUTPUT:
// Hello, Alice! Welcome to our website.
// Hello, Bob! Welcome to our website.
// Hello, Charlie! Welcome to our website.